public class operatortest {
   public static void main(String[] args)
   {
     System.out.println(2 + 3);
     System.out.println(2 - 3);
     System.out.println(2 * 3);
     System.out.println(2 / 3);
     // == is to test while = is to assign
     System.out.println(2 == 3);
     System.out.println(2 != 3);
   }
}